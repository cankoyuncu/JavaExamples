public class Main {
    public static void main(String[] args) {

        //For Loop

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        System.out.println("For Dongusu Bitti!");


        //While Loop
        int i=1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        System.out.println("While Dongusu Bitti!");

        //Do-While Loop
        int j=1;
        do {
            System.out.println(j);
            j+=2;
        } while(j<10);

        System.out.println("Do-While Dongusu Bitti!");
    }
}